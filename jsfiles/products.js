let storedata = JSON.parse(localStorage.getItem("shoppingdata")) || [];
let wishlistData = JSON.parse(localStorage.getItem("wishlistData")) || [];

// Function to update badges
function updateBadges() {
  const cartBadge = document.getElementById("cart-badge");
  const wishlistBadge = document.getElementById("wishlist-badge");

  // Update Cart Badge Count
  const totalCartItems = storedata.reduce((total, item) => total + item.quantity, 0);
  cartBadge.textContent = totalCartItems;

  // Update Wishlist Badge Count
  wishlistBadge.textContent = wishlistData.length;
}

// Function to show notifications
function showNotification(message) {
  const notificationEl = document.getElementById("notification");
  notificationEl.textContent = message;
  notificationEl.classList.add("show");

  // Hide the notification after 2 seconds
  setTimeout(() => {
    notificationEl.classList.remove("show");
  }, 2000);
}

// Function to load products with search functionality
function loadProducts() {
  const category = localStorage.getItem("selectedCategory");
  const searchQuery = localStorage.getItem("searchQuery");
  const container = document.getElementById("productContainer");

  // Clear the search query from localStorage to avoid stale data
  localStorage.removeItem("searchQuery");

  let products = [];
  if (category && productsData[category]) {
    products = productsData[category]; // Get products for the selected category
  } else {
    // Combine all products if no category is selected
    products = Object.values(productsData).flat();
  }

  // If a search query exists, filter products based on the query
  if (searchQuery) {
    products = products.filter((product) =>
      product.name.toLowerCase().includes(searchQuery) ||
      product.description.toLowerCase().includes(searchQuery) ||
      product.category.toLowerCase().includes(searchQuery)
    );

    // Show a message if no products match the search query
    if (products.length === 0) {
      container.innerHTML = `<p>No results found for "${searchQuery}".</p>`;
      return;
    }
  }

  // Display the products
  container.innerHTML = ""; // Clear previous content
  products.forEach((product) => {
    const productCard = document.createElement("div");
    productCard.className = "product-card";
    productCard.innerHTML = `
      <img src="${product.image}" alt="${product.name}">
      <h3>${product.name}</h3>
      <p>${product.description}</p>
      <p class="price"><strong></strong>â‚¹${product.price}</p>
      <button class="add-to-cart-btn">Add to Cart</button>
      <button class="add-to-wishlist-btn">Wishlist</button>
    `;
    container.appendChild(productCard);

    // Add event listener to "Add to Cart" button
    const addToCartBtn = productCard.querySelector(".add-to-cart-btn");
    addToCartBtn.addEventListener("click", () => {
      const newProduct = {
        name: product.name,
        image: product.image,
        price: product.price,
        category: category || "all",
        description: product.description,
        quantity: 1,
      };

      const existingProductIndex = storedata.findIndex(
        (item) => item.name === newProduct.name && item.category === newProduct.category
      );

      if (existingProductIndex > -1) {
        storedata[existingProductIndex].quantity += 1;
      } else {
        storedata.push(newProduct);
      }

      localStorage.setItem("shoppingdata", JSON.stringify(storedata));
      showNotification("Item added to cart!");
      updateBadges();
    });

    // Add event listener to "Add to Wishlist" button
    const addToWishlistBtn = productCard.querySelector(".add-to-wishlist-btn");
    addToWishlistBtn.addEventListener("click", () => {
      if (!wishlistData.some((item) => item.name === product.name)) {
        wishlistData.push(product);
        localStorage.setItem("wishlistData", JSON.stringify(wishlistData));
        showNotification("Item added to wishlist!");
        updateBadges();
      } else {
        showNotification("Item already in wishlist!");
      }
    });
  });
}

// Initial Load
window.onload = () => {
  loadProducts(); // Load products initially when the page loads
  updateBadges(); // Update cart and wishlist badges
};
